public class LoopExercises {

    public static void main(String[] args) {
        int i = 45;

        while(i < 51) {
            System.out.println(i);
            i++;
        }

        for (int j = 1; j < 7; j++) {
            if(j%2 == 0) {
                System.out.println(j);
            }
        }

    }

}